CREATE TABLE PUBLIC.T_SALE_ORDER_DETAIL (
	ID INTEGER NOT NULL IDENTITY,
	CUSTOMER INTEGER NOT NULL REFERENCES T_CUSTOMER_DETAIL(ID),
	PRIMARY KEY (ID)
);

CREATE TABLE PUBLIC.T_ORDER_LINE_DETAIL (
	ID INTEGER NOT NULL IDENTITY,
	PRODUCT INTEGER NOT NULL REFERENCES T_PRODUCT_DETAIL(ID),
